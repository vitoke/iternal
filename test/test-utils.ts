export const addIndex = (v: number, i: number) => v + i + 1
export const isEven = (v: number) => v % 2 === 0
export const isOdd = (v: number) => v % 2 === 1
export const less3 = (v: number) => v < 3
export const isNegative = (v: number) => v < 0
export const isPositive = (v: number) => v >= 0
export const sum = (a: number, b: number) => a + b
